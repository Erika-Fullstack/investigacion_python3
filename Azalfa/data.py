# Example of different primitive data types in Python

integer_example = 42
float_example = 7.89
char_example = 'X'
string_example = "Learning Python"
boolean_example = False
none_example = None

print("Integer:", integer_example)  # output  Integer: 42
print("Float:", float_example) # output Float: 7.89
print("Character:", char_example) # output  Character: X
print("String:", string_example) # output String: Learning Python
print("Boolean:", boolean_example) # output Boolean: False
print("None:", none_example) # output None: None


#Basic Type Conversion in python
num_str = "123.45"
num_float = float(num_str)  # Convierte de string a flotante
num_int = int(num_float)    # Convierte de flotante a entero
num_bool = bool(num_int)    # Convierte de entero a booleano

print("String:", num_str) # output String: 123.45
print("Float:", num_float)# output Float: 123.45
print("Integer:", num_int)# output Integer: 123
print("Boolean:", num_bool)# output Boolean: True



